var mongoose = require('mongoose');

// Model: Todo, using validators and default values.
var Todo = mongoose.model('Todo', {
  text: {
    type: String,
    required: true,
    minlength: 1,
    trim: true
  },
  completed: {
    type: Boolean,
    default: false
  },
  completedAt: {
    type: Number,
    default: null
  }
});

//Export it as a separate module.
module.exports = {Todo};
